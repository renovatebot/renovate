import { hashicorp2npm, npm2hashicorp } from './convertor';

describe('modules/versioning/hashicorp/convertor', () => {
  test.each`
    constraint          | expected
    ${'4.2.0'}          | ${'4.2.0'}
    ${'4.2.0-alpha'}    | ${'4.2.0-alpha'}
    ${'~> 4'}           | ${'>=4'}
    ${'~> 4.0'}         | ${'^4.0'}
    ${'~> 4.0.0'}       | ${'~4.0.0'}
    ${'~> 4.0.1'}       | ${'~4.0.1'}
    ${'~> 4.1'}         | ${'^4.1'}
    ${'~> 4.1.0'}       | ${'~4.1.0'}
    ${'~> 4.1.1'}       | ${'~4.1.1'}
    ${'~> 4.0.0-alpha'} | ${'~4.0.0-alpha'}
    ${'>= 4.0'}         | ${'>=4.0'}
    ${'>=4.0'}          | ${'>=4.0'}
    ${'<= 4.0'}         | ${'<=4.0'}
    ${'<=4.0'}          | ${'<=4.0'}
    ${'> 4.0'}          | ${'>4.0'}
    ${'< 4.0'}          | ${'<4.0'}
    ${'= 4.0'}          | ${'4.0'}
    ${'> 4.0, < 5.0'}   | ${'>4.0 <5.0'}
    ${'> 4.0,< 5.0'}    | ${'>4.0 <5.0'}
    ${'~> 2.3.4'}       | ${'~2.3.4'}
  `(
    'hashicorp2npm("$constraint") === $expected',
    ({ constraint, expected }) => {
      expect(hashicorp2npm(constraint)).toBe(expected);
    }
  );

  test.each`
    expected              | constraint
    ${'4.2.0'}            | ${'4.2.0'}
    ${'4.2.0-alpha'}      | ${'4.2.0-alpha'}
    ${'4.0'}              | ${'4.0'}
    ${'~> 4.0'}           | ${'^4'}
    ${'~> 4.0'}           | ${'^4.0'}
    ${'~> 4.0'}           | ${'^4.0.0'}
    ${'~> 4.1'}           | ${'^4.1'}
    ${'~> 4.1'}           | ${'^4.1.0'}
    ${'~> 4.1, >= 4.1.1'} | ${'^4.1.1'}
    ${'~> 4.0'}           | ${'~4'}
    ${'~> 4.0.0'}         | ${'~4.0'}
    ${'~> 4.0.0'}         | ${'~4.0.0'}
    ${'~> 4.0.1'}         | ${'~4.0.1'}
    ${'~> 4.1.0'}         | ${'~4.1.0'}
    ${'~> 4.1.1'}         | ${'~4.1.1'}
    ${'~> 4.0.0-alpha'}   | ${'~4.0.0-alpha'}
    ${'>= 4.0'}           | ${'>=4.0'}
    ${'<= 4.0'}           | ${'<=4.0'}
    ${'> 4.0'}            | ${'>4.0'}
    ${'< 4.0'}            | ${'<4.0'}
    ${'> 4.0, < 5.0'}     | ${'>4.0 <5.0'}
    ${'~> 2.3.4'}         | ${'~2.3.4'}
  `(
    'npm2hashicorp("$constraint") === $expected',
    ({ expected, constraint }) => {
      expect(npm2hashicorp(constraint)).toBe(expected);
    }
  );
});
